char	*ft_strrchr(const char *s, int c)
{
	char *ret;

	ret = 0;
	while (*s)
	{
		if (*s == c)
		{
			ret = (char*)s;
		}
		++s;
	}
	if (ret)
		return (ret);
	if (c == '\0')
	{
		return ((char*)s);
	}
	return (0);
}
