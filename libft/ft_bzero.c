#include "libft.h"

void	ft_bzero(void *b, size_t len)
{
	unsigned char	*p;
	size_t			i;

	p = (unsigned char*)b;
	i = 0;
	while (i < len)
	{
		p[i] = 0;
		i++;
	}
}
